my_dict = {
    'name': 'Alice',
    'age': 30,
    'city': 'New York',
    'occupation': 'Engineer',
    'country': 'USA'
}

print("Keys:", my_dict.keys())

print("Values:", my_dict.values())

print("Key-Value Pairs:", my_dict.items())

print("\nIterating through the dictionary:")
for key, value in my_dict.items():
    print(f"The {key} is {value}")

