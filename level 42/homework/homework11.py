# Creating a dictionary with at least five key-value pairs
my_dictionary = {
    "name": "John",
    "age": 25,
    "city": "Tbilisi",
    "country": "Georgia",
    "occupation": "Engineer"
}

# Printing the length of the dictionary using the len() function
length = len(my_dictionary)
print(f"The length of the dictionary is: {length}")
